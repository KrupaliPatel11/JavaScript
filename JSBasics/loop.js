// while loop-first check the condition if condition then execute the code
let i =0;
while(i < 3) {
    console.log(i);
    i++;
}

console.log("");

// do-while will first run code once then check condition if true then run code again and again until condition is true
let j = 0;
do{
    console.log(j);
    j++;
} while(j < 3);

